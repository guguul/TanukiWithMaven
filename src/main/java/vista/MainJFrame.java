    private void continuar_temaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continuar_temaActionPerformed
        modelo_tanuki.Tema temaPadre = (modelo_tanuki.Tema) cboTemaPadre.getSelectedItem();
        if (temaPadre == null) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un tema para continuar.");
            return;
        }

        // OBTIENE LOS TEMAS HIJOS
        List<modelo_tanuki.Tema> temasHijo = temaPadre.getTemasHijos();

        // LLENA EL SEGUNDO COMBOBOX (cboTemaHijo)
        cboTemaHijo.removeAllItems();
        if (temasHijo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Este tema no tiene sub-temas para practicar.");
            return;
        }
        for (modelo_tanuki.Tema t : temasHijo) {
            cboTemaHijo.addItem(t);
        }
        temaPadreSelecc.setText(temaPadre.getNombre());
        // MESTRA EL PANEL "subtemaEst"
        CardLayout cardLayout = (CardLayout) this.cardPracticaEst.getLayout();
        cardLayout.show(cardPracticaEst,"subtemaEst");
        this.revalidate(); // Recalcula el layout
        this.repaint();  // Redibuja la ventana
    }//GEN-LAST:event_continuar_temaActionPerformed

