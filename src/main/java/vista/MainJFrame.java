    private void consultarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarReporteActionPerformed
        try {
            // 1. Lee los JComboBoxes
            String gradoStr = (String) cboReporteGrado.getSelectedItem();
            String seccionStr = (String) cboReporteSeccion.getSelectedItem();
            String tipoConsulta = (String) cboReporteTipoConsulta.getSelectedItem();

            if (gradoStr == null || seccionStr == null || tipoConsulta == null) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
                return;
            }

            // 2. Busca el Saln
            int grado = Integer.parseInt(gradoStr);
            Salon salon = controlador.buscarSalon(grado, seccionStr);
          
            if (salon == null) {
                JOptionPane.showMessageDialog(this, "Saln no encontrado.");
                return;
            }
                
            String periodoStr = (String) cboReportePeriodo.getSelectedItem();
            this.periodoParaReporte = getPeriodoEnumFromString(periodoStr);
            this.salonParaReporte = salon; 
            this.estudianteParaReporte = null;

            
            CardLayout cardLayout = (CardLayout) cardReportesMaestro.getLayout();
            
            // 5. Decide qu panel mostrar
            if (tipoConsulta.equals("ranking")) {

                boolean exito = controlador.mostrarRankingSalon(salon, periodoParaReporte, tablaRankingSalon);

                if (exito) {
                    cardLayout.show(cardReportesMaestro, "rankingSalonReportesMaestro"); 
                } else {
                    JOptionPane.showMessageDialog(this, "Ningn estudiante en este saln tiene progreso registrado.");
                }

            } else if (tipoConsulta.equals("detallada")) {
                this.salonParaReporte = salon;
                cardLayout.show(cardReportesMaestro, "detalleSalonReportesMaestro");
            }

            this.revalidate();
            this.repaint();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: El grado '" + cboReporteGrado.getSelectedItem() + "' no es un nmero.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri un error inesperado: " + e.getMessage());
        }
    }//GEN-LAST:event_consultarReporteActionPerformed

