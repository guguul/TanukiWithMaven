    private void cboTemaPadreItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboTemaPadreItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {

            // obtiene el Tema PADRE seleccionado
            modelo_tanuki.Tema temaPadre = (modelo_tanuki.Tema) cboTemaPadre.getSelectedItem();
            if (temaPadre == null) return;

            // obtiene los temas HIJOS (ej: Suma, Resta)
            List<modelo_tanuki.Tema> temasHijo = temaPadre.getTemasHijos();

            // llena el cboTemaHijo
            cboTemaHijo.removeAllItems();
            for (modelo_tanuki.Tema t : temasHijo) {
                cboTemaHijo.addItem(t);
            }
        }
    }//GEN-LAST:event_cboTemaPadreItemStateChanged

